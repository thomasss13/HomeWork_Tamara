import random

dict = {}

words = ["оператор", "конструкция", "объект"]
desc_  = [
    "Это слово обозначает наименьшую автономную часть языка программирования",
    "Это совокупность объявлений данных и функций, объединенных в единую структуру",
    "Это сущность в программировании, обладающая собственным состоянием и поведением"
]

word_dict = {}
for i in range(len(words)):
    word_dict[words[i]] = desc_[i]

secret_word = random.choice(list(word_dict.keys()))
description = word_dict[secret_word]

field = ['*'] * len(secret_word)
tries = 10
guessed_letters = []

print(description)
print(field)

while tries > 0 and '*' in field:
    letter = input("Введите букву: ")

    if len(letter) != 1:
        print("Нужно ввести одну букву!")
        continue

    if letter in guessed_letters:
        print("Вы уже называли эту букву!")
        continue

    guessed_letters.append(letter)
    matched = False

    for i in range(len(secret_word)):
        if secret_word[i] == letter:
            field[i] = letter
            matched = True

    if matched:
        print(field)
        if '*' not in field:
            print("ПРИЗЫ В СТУДИЮ!")
    else:
        tries -= 1
        print("Нет такой буквы.")

        if tries > 0:
            print(f"У вас осталось {tries} попыток !")
        else:
            print("БАНКРОТ!")
            print(f"Загаданное слово было: {secret_word}")